package com.hello.operator;

public class RelateDemo {
	public static void main(String[] args) {
		int a=3,b=5;
		System.out.println("a<b="+(a<b));
		System.out.println("a>b="+(a>b));
		int n=8;
		float m = 0.4f;
		if(n<10 && m<0.3){
			System.out.println(3);
		}
		if(n<10) {
			System.out.println(1);
		}
		if(m <0.3) {
			System.out.println(2);
		}
		
	}
} 